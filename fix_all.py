with open("public/booking.html", "rb") as f:
    content = f.read()

# Fix all common double-encoded UTF-8 patterns at once
replacements = [
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x82\xc2\xac\xc3\xa2\xc2\x80\xc2\x9c', b'"'),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x82\xc2\xac\xc3\xa2\xc2\x80\xc2\x9d', b'"'),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x82\xc2\xac\xc3\xa2\xc2\x80\xc2\x99', b"'"),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x82\xc2\xac\xc3\xa2\xc2\x80\xc2\x9e', b'"'),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x82\xc2\xac', b'-'),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x80\xc2\x9e\xc3\x82\xc2\xa2', b'TM'),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x80\xc2\x99', b"'"),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x80\xc2\x93', b'-'),
    (b'\xc3\x83\xc2\xa2\xc3\xa2\xc2\x80\xc2\x94', b'-'),
    (b'\xc3\x82\xc2\xb7', b' - '),
    (b'\xc3\x82\xc2\xa9', b'(c)'),
    (b'\xc3\x82\xc2\xae', b'(R)'),
    (b'\xc3\x83\xc2\xb0\xc5\xb8\xc5\x92\xc2\xb4', b''),
    (b'\xc3\x83\xc2\xb0\xc5\xb8', b''),
    (b'\xc3\x83\xc2\x82\xc3\x82\xc2\xb7', b' - '),
    (b'\xc3\x82\xc2\xb0', b''),
    (b'\xc3\x83\xc2\x97', b'x'),
    (b'\xc3\x83\xc2\x80', b''),
]

for old, new in replacements:
    count = content.count(old)
    if count > 0:
        print(f"Replacing {count}x: {repr(old[:6])}... -> {repr(new)}")
        content = content.replace(old, new)

with open("public/booking.html", "wb") as f:
    f.write(content)
print("Done!")
